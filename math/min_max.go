package math

//-------------------------- int ------------------------------------------------------------------

func IntMin(a, b int) int {
	if a <= b {
		return a
	} else {
		return b
	}
}

func IntMax(a, b int) int {
	if a >= b {
		return a
	} else {
		return b
	}
}

